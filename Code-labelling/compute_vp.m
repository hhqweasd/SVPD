

a1x=1573
a1y=2021
a2x=49
a2y=765

b1x=1789
b1y=1377
b2x=25
b2y=553

X1 = [a1x a1y];
Y1 = [a2x a2y];
%line2
X2 = [b1x b1y];
Y2 = [b2x b2y];
if X1(1)==Y1(1)
   X=X1(1);
   k2=(Y2(2)-X2(2))/(Y2(1)-X2(1));
   b2=X2(2)-k2*X2(1); 
   Y=k2*X+b2;
end
if X2(1)==Y2(1)
   X=X2(1);
   k1=(Y1(2)-X1(2))/(Y1(1)-X1(1));
   b1=X1(2)-k1*X1(1);
   Y=k1*X+b1;
end
if X1(1)~=Y1(1)&X2(1)~=Y2(1)
   k1=(Y1(2)-X1(2))/(Y1(1)-X1(1));
   k2=(Y2(2)-X2(2))/(Y2(1)-X2(1));
   b1=X1(2)-k1*X1(1);
   b2=X2(2)-k2*X2(1);
    if k1==k2
      X=[];
      Y=[];
   else
   X=(b2-b1)/(k1-k2);
   Y=k1*X+b1;
   end
end
fprintf('%.0f %.0f\n',X,Y)